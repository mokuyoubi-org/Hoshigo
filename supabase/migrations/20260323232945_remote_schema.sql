    users.points_to_gumi(r.white_points)
  FROM game.records r
  LEFT JOIN users.profiles bp ON bp.uid = r.black_uid
  LEFT JOIN users.profiles wp ON wp.uid = r.white_uid
  WHERE r.black_uid = p_uid OR r.white_uid = p_uid
  ORDER BY r.id DESC
  LIMIT p_limit
  OFFSET p_offset;
