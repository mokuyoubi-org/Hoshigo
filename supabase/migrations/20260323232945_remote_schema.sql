CREATE OR REPLACE FUNCTION game.on_move_added()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
declare
  v_updated game.playing%rowtype;
begin
  update game.playing set
    turn             = case when OLD.turn = 'black' then 'white' else 'black' end,
    turn_switched_at = now(),
    black_seconds    = case
      when OLD.turn = 'black'
      then OLD.black_seconds - extract(epoch from (now() - OLD.turn_switched_at))::int + 1
      else OLD.black_seconds
    end,
    white_seconds    = case
      when OLD.turn = 'white'
      then OLD.white_seconds - extract(epoch from (now() - OLD.turn_switched_at))::int + 1
      else OLD.white_seconds
    end
  where id = NEW.id
  returning * into v_updated;

  perform realtime.send(
    jsonb_build_object(
      'move',          NEW.moves[array_length(NEW.moves, 1)],
      'move_count',    array_length(NEW.moves, 1),
      'turn',          v_updated.turn,
      'black_seconds', v_updated.black_seconds,
      'white_seconds', v_updated.white_seconds
    ),
    'move', -- гӮӨгғҷгғігғҲеҗҚ
    'game:' || NEW.id::text, -- гғҒгғЈгғігғҚгғ«еҗҚ
    false
  );

  return null;
end;
$function$
;

CREATE OR REPLACE FUNCTION game.resign(p_match_id integer, p_color text)
 RETURNS void
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
begin
  -- жң¬дәәгғҒгӮ§гғғгӮҜ
  if p_color = 'black' then
    if (select black_uid from game.playing where id = p_match_id) != auth.uid() then
      raise exception 'дёҚжӯЈгҒӘгӮўгӮҜгӮ»гӮ№';
    end if;
  else
    if (select white_uid from game.playing where id = p_match_id) != auth.uid() then
      raise exception 'дёҚжӯЈгҒӘгӮўгӮҜгӮ»гӮ№';
    end if;
  end if;

  update game.playing set
    result = case when p_color = 'black' then 'W+R' else 'B+R' end,
    status = 'finished'
  where id = p_match_id
    and status = 'playing';
end;
$function$
;

CREATE OR REPLACE FUNCTION game.server_callback(p_match_id integer DEFAULT NULL::integer, p_move smallint DEFAULT NULL::smallint, p_dead_stones smallint[] DEFAULT NULL::smallint[], p_result text DEFAULT NULL::text, p_analysis_id integer DEFAULT NULL::integer, p_analysis_result jsonb DEFAULT NULL::jsonb)
 RETURNS void
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
begin
  -- гғңгғғгғҲгҒ®зқҖжүӢпјҲturn / turn_switched_at / secondsгҒҜгғҲгғӘгӮ¬гғјгҒ«е§”иӯІпјү
  if p_match_id is not null and p_move is not null and p_result is null then
    update game.playing set
      moves = array_append(coalesce(moves, '{}'), p_move)
    where id = p_match_id;
    return;
  end if;
 
  -- еҜҫеұҖзөӮдәҶ
  if p_match_id is not null and p_result is not null then
    update game.playing set
      moves       = case when p_move is not null then array_append(coalesce(moves, '{}'), p_move) else moves end,
      dead_stones = p_dead_stones,
      result      = p_result,
      status      = 'finished'
    where id = p_match_id;
    return;
  end if;
 
  -- и©°зўҒи§Јжһҗзөҗжһң
  if p_analysis_id is not null then
    update server.queue set
      result = p_analysis_result::text,
      status = 'done'
    where id = p_analysis_id;
    return;
  end if;
end;
$function$
;

CREATE OR REPLACE FUNCTION game.time_connection_check()
 RETURNS void
 LANGUAGE plpgsql
AS $function$
declare
  rec record; -- playingеҶ…гҒ«гҒӮгӮӢmatchгҒҢдёҖгҒӨгҒҡгҒӨе…ҘгӮӢгҖӮ
  v_is_black_bot boolean; -- вҡ«пёҸгҒҢbotгҒӢгҒ©гҒҶгҒӢ
  v_is_white_bot boolean; -- вҡӘпёҸгҒҢbotгҒӢгҒ©гҒҶгҒӢ
begin
  -- playingеҶ…гҒ«гҒӮгӮӢmatchгӮ’дёҖгҒӨгҒҡгҒӨиҰӢгҒҰгҒ„гҒҸ
  for rec in
    select *
    from game.playing
    where status = 'playing'
  loop

    -- ------------------------------
    -- botеҲӨе®ҡ
    -- ------------------------------
    select is_bot into v_is_black_bot -- вҡ«пёҸгҒҢbotгҒӢгҒ©гҒҶгҒӢ
    from users.profiles
    where uid = rec.black_uid;

    select is_bot into v_is_white_bot -- вҡӘпёҸгҒҢbotгҒӢгҒ©гҒҶгҒӢ
    from users.profiles
    where uid = rec.white_uid;

    -- ------------------------------
    -- жҷӮй–“еҲҮгӮҢеҲӨе®ҡ
    -- ------------------------------
    if (not v_is_black_bot) and -- вҡ«пёҸгҒҢгғңгғғгғҲгҒ§гҒҜгҒӘгҒҸгҖҒ
      rec.turn = 'black' and -- вҡ«пёҸгҒ®з•ӘгҒ§гҖҒ
      rec.black_seconds - extract(epoch from (now() - rec.turn_switched_at)) < 0 then -- вҡ«пёҸгҒ®ж®ӢгӮҠжҷӮй–“ - (зҸҫеңЁжҷӮеҲ»-жүӢз•ӘеҲҮжӣҝжҷӮеҲ») < 0гҒӘгӮү
      update game.playing
      set status = 'finished',
          result = 'W+T'
      where id = rec.id;

    elsif (not v_is_white_bot) and -- вҡӘпёҸгҒҢгғңгғғгғҲгҒ§гҒҜгҒӘгҒҸгҖҒ
      rec.turn = 'white' and -- вҡӘпёҸгҒ®з•ӘгҒ§гҖҒ
      rec.white_seconds - extract(epoch from (now() - rec.turn_switched_at)) < 0 then -- вҡӘпёҸгҒ®ж®ӢгӮҠжҷӮй–“ - (зҸҫеңЁжҷӮеҲ»-жүӢз•ӘеҲҮжӣҝжҷӮеҲ») < 0гҒӘгӮү
      update game.playing
