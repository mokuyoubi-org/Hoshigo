CREATE OR REPLACE FUNCTION users.add_icons(p_uid uuid, p_new_gumi smallint, p_games integer)
 RETURNS smallint[]
 LANGUAGE plpgsql
AS $function$
declare
  v_added int2[] := '{}';
  v_current int2[];
begin
  -- зҸҫеңЁгҒ®iconsеҸ–еҫ—
  select acquired_icons
    into v_current
  from users.profiles
  where uid = p_uid;

  if v_current is null then
    v_current := '{}';
  end if;

  -- гҒ“гҒ“гҒҫгҒ§гҒ§й…ҚеҲ—гҒ®жә–еӮҷе®ҢдәҶ

  -- гӮўгӮӨгӮігғі0~11гҒҫгҒ§гҖӮгҒ—гӮҚгҒҗгҒҝ~гҒ«гҒҳгҒҗгҒҝ3гҒҫгҒ§гҖӮ
  for i in 0..11 loop
