CREATE OR REPLACE FUNCTION game.check_can_play()
 RETURNS jsonb
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
declare
  v_profile record;
  v_app_status record;
begin

  -- гғ‘гӮҝгғјгғі1. гғЎгғігғҶгғҠгғігӮ№дёӯгҒ гҒЈгҒҹ
  select maintenance, message
  into v_app_status
  from system.get_app_status();
  if v_app_status.maintenance then
    return jsonb_build_object(
      'can_play', false,
      'reason', 'maintenance',
      'message', v_app_status.message
    );
  end if;

  -- гғ‘гӮҝгғјгғі2. еҜҫеұҖж•°дёҠйҷҗгҒ«йҒ”гҒ—гҒҰгҒҹ
  select is_premium, daily_play_count
  into v_profile
  from users.profiles
  where uid = auth.uid();
  if not v_profile.is_premium and v_profile.daily_play_count >= 10 then
    return jsonb_build_object('can_play', false, 'reason', 'daily_limit');
  end if;

  -- гғ‘гӮҝгғјгғі3. йҒҠгҒ№гӮӢпјҒ
  return jsonb_build_object('can_play', true);
end;
$function$
;

CREATE OR REPLACE FUNCTION game.get_records_with_profiles(p_uid uuid, p_limit integer, p_offset integer)
 RETURNS TABLE(id integer, created_at date, result text, match_type smallint, moves integer[], dead_stones integer[], black_points smallint, white_points smallint, black_uid uuid, black_username text, black_displayname text, black_icon_index smallint, black_gumi_index smallint, white_uid uuid, white_username text, white_displayname text, white_icon_index smallint, white_gumi_index smallint)
 LANGUAGE sql
 STABLE SECURITY DEFINER
 SET search_path TO 'game', 'users'
AS $function$
  SELECT
    r.id,
    r.created_at,
    r.result,
    r.match_type,
    r.moves,
    r.dead_stones,
    r.black_points,
    r.white_points,
    r.black_uid,
    bp.username,
    bp.displayname,
    bp.icon_index,
    users.points_to_gumi(r.black_points),
    r.white_uid,
    wp.username,
    wp.displayname,
