CREATE OR REPLACE FUNCTION game.add_move(p_match_id integer, p_move smallint, p_color text)
 RETURNS void
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
declare
  v_match game.playing%rowtype;
  v_bot_uid uuid;
  v_is_bot boolean;
  v_bot_color text;
  v_bot_points smallint;
  v_engine text;
  v_fly_url text;
  v_fly_secret text;
  v_is_second_pass boolean;
begin
  -- гғһгғғгғҒеҸ–еҫ—
  select * into v_match
  from game.playing
  where id = p_match_id;
 
  if not found then
    raise exception 'гғһгғғгғҒгҒҢиҰӢгҒӨгҒӢгӮҠгҒҫгҒӣгӮ“';
  end if;
 
  -- жүӢз•ӘгғҒгӮ§гғғгӮҜ
  if v_match.turn != p_color then
    raise exception 'жүӢз•ӘгҒ§гҒҜгҒӮгӮҠгҒҫгҒӣгӮ“';
  end if;
 
  -- жң¬дәәгғҒгӮ§гғғгӮҜ
  if p_color = 'black' then
    if v_match.black_uid != auth.uid() then
      raise exception 'дёҚжӯЈгҒӘгӮўгӮҜгӮ»гӮ№';
    end if;
  else
    if v_match.white_uid != auth.uid() then
      raise exception 'дёҚжӯЈгҒӘгӮўгӮҜгӮ»гӮ№';
    end if;
  end if;
 
  -- 2йҖЈз¶ҡгғ‘гӮ№гғҒгӮ§гғғгӮҜ
  v_is_second_pass := (
    p_move = -1 and
    array_length(v_match.moves, 1) > 0 and
    v_match.moves[array_length(v_match.moves, 1)] = -1
  );
 
  -- movesгӮ’жӣҙж–°пјҲturn / turn_switched_at / secondsгҒҜгғҲгғӘгӮ¬гғјгҒ«е§”иӯІпјү
  update game.playing set
    moves            = array_append(coalesce(moves, '{}'), p_move),
    black_last_seen  = case when p_color = 'black' then now() else black_last_seen end,
    white_last_seen  = case when p_color = 'white' then now() else white_last_seen end
  where id = p_match_id;
 
  -- VaultгҒӢгӮүеҸ–еҫ—
  select decrypted_secret into v_fly_url
  from vault.decrypted_secrets where name = 'supabase_to_fly_url';
 
  select decrypted_secret into v_fly_secret
  from vault.decrypted_secrets where name = 'supabase_to_fly_secret';
 
  -- 2йҖЈз¶ҡгғ‘гӮ№ вҶ’ flyгҒ®/scoreгӮ’е‘јгҒ¶
  if v_is_second_pass then
    perform net.http_post(
      url := v_fly_url || '/score',
      body := jsonb_build_object(
        'match_id', p_match_id,
        'moves', array_append(coalesce(v_match.moves, '{}'), p_move),
        'match_type', v_match.match_type
      ),
      headers := jsonb_build_object(
        'Content-Type', 'application/json',
        'Authorization', 'Bearer ' || v_fly_secret
      )
    );
    return;
  end if;
 
  -- зӣёжүӢгҒҢbotгҒӢгғҒгӮ§гғғгӮҜ
  v_bot_color := case when p_color = 'black' then 'white' else 'black' end;
  v_bot_uid := case
    when v_bot_color = 'white' then v_match.white_uid
    else v_match.black_uid
  end;
  v_bot_points := case
    when v_bot_color = 'white' then v_match.white_points
    else v_match.black_points
  end;
 
  select is_bot into v_is_bot
  from users.profiles
  where uid = v_bot_uid;
 
  -- зӣёжүӢгҒҢдәәй–“гҒӘгӮүзөӮдәҶ
  if not v_is_bot then
    return;
  end if;
 
  -- engineгӮ’pointsгҒ§йҒёжҠһ
  v_engine := case
    when v_bot_points = 420 then 'gnugo' -- гҒҶгҒ©гӮ“
    when v_bot_points = 560 then 'pachi' -- гҒҠгҒӣгӮ“гҒ№гҒ„
    when v_bot_points = 720 then 'katago-b6c96' -- гҒӣгҒӘ
    when v_bot_points = 900 then 'leela' -- гӮӢгҒӘ
    else 'katago-b10c128' -- гҒҶгҒ•гҒҺе…Ҳз”ҹ
  end;
 
  -- flyгҒ«гғӘгӮҜгӮЁгӮ№гғҲ
  perform net.http_post(
    url := v_fly_url || '/play/' || v_engine,
    body := jsonb_build_object(
      'match_id', p_match_id,
      'moves', array_append(coalesce(v_match.moves, '{}'), p_move),
      'color', v_bot_color,
      'match_type', v_match.match_type
    ),
    headers := jsonb_build_object(
      'Content-Type', 'application/json',
      'Authorization', 'Bearer ' || v_fly_secret
    )
  );
end;
$function$
;

CREATE OR REPLACE FUNCTION game.archive_playing()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
declare
  v_black_delta smallint := 0; -- вҡ«пёҸгҒҢдёҠгҒҢгӮӢгӮӮгҒ—гҒҸгҒҜдёӢгҒҢгӮӢpointж•°
  v_white_delta smallint := 0; -- вҡӘпёҸгҒҢдёҠгҒҢгӮӢгӮӮгҒ—гҒҸгҒҜдёӢгҒҢгӮӢpointж•°
  v_diff smallint; -- дәҢдәәгҒ®pointе·®
  v_black_games int; -- вҡ«пёҸгҒ®еҜҫеұҖж•°
  v_white_games int; -- вҡӘпёҸгҒ®еҜҫеұҖж•°
  v_is_black_bot boolean; -- вҡ«пёҸгҒҢbotгҒӢгҒ©гҒҶгҒӢ
  v_is_white_bot boolean; -- вҡӘпёҸгҒҢbotгҒӢгҒ©гҒҶгҒӢ
  v_black_points smallint; -- вҡ«пёҸгҒ®points
  v_white_points smallint; -- вҡӘпёҸгҒ®points
  v_black_giantkill int; -- вҡ«пёҸгҒ®ж јдёҠйҖЈеӢқж•°
  v_white_giantkill int; -- вҡӘпёҸгҒ®ж јдёҠйҖЈеӢқж•°
  v_black_new_points smallint; -- вҡ«пёҸгҒ®ж–°гҒ—гҒ„points
  v_white_new_points smallint; -- вҡӘпёҸгҒ®ж–°гҒ—гҒ„points
  v_black_old_gumi smallint; -- вҡ«пёҸгҒ®е…ғгҖ…гҒ®gumi
  v_white_old_gumi smallint; -- вҡӘпёҸгҒ®е…ғгҖ…гҒ®gumi
  v_black_new_gumi smallint; -- вҡ«пёҸгҒ®ж–°гҒ—гҒ„gumi
  v_white_new_gumi smallint; -- вҡӘпёҸгҒ®ж–°гҒ—гҒ„gumi
  v_black_new_icons smallint[]; -- вҡ«пёҸгҒ®ж–°гҒ—гҒҸзҚІеҫ—гҒ—гҒҹiconгҒҹгҒЎ
  v_white_new_icons smallint[]; -- вҡӘпёҸгҒ®ж–°гҒ—гҒҸзҚІеҫ—гҒ—гҒҹiconгҒҹгҒЎ
begin
  -- гҒҫгҒҡгҖҒгғ—гғӯгғ•гӮЈгғјгғ«гҒӢгӮүжғ…е ұгӮ’еҸ–гҒЈгҒҰгҒҚгҒҰгҖҒеӨүж•°гҒ«ж јзҙҚгҒ—гҒҰгҒҠгҒҸ
  select
    games, points, giantkill_stroke, is_bot
  into
    v_black_games, v_black_points, v_black_giantkill, v_is_black_bot
  from users.profiles
  where uid = new.black_uid;

  select
    games, points, giantkill_stroke, is_bot
  into
    v_white_games, v_white_points, v_white_giantkill, v_is_white_bot
  from users.profiles
  where uid = new.white_uid;

  -- в”Җв”Җв”Җ гғқгӮӨгғігғҲиЁҲз®— в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
  -- ------------------
  -- вҡ«пёҸгҒҢеӢқгҒЈгҒҹрҹҺү
  -- ------------------
  if new.result like 'B+%' then  

  -- еӢқгҒЈгҒҹвҡ«пёҸеҒҙгҒ®еҮҰзҗҶ
  v_diff := new.white_points - new.black_points; -- дәҢдәәгҒ®pointsгҒ®е·®
  v_black_delta := greatest(1, least(19,
    10 + (v_diff + case when v_diff >= 0 then 50 else -50 end) / 100
  )); -- гҒқгӮҢгҒһгӮҢеў—жёӣгҒҷгӮӢpoint
  if v_diff > 0 then -- ж јдёҠгҒ®вҡӘпёҸгҒ«еӢқгҒЈгҒҹ!зӣёжүӢгҒҢгғңгғғгғҲгҒ§гӮӮй–ўдҝӮгҒӘгҒ„
    v_black_giantkill := v_black_giantkill + 1; -- ж јдёҠйҖЈеӢқ++
    v_black_delta := v_black_delta * v_black_giantkill; -- ж јдёҠйҖЈеӢқгғңгғјгғҠгӮ№
  elsif v_is_white_bot then -- вҡӘпёҸгҒҢгғңгғғгғҲгҒ гҒЈгҒҹе ҙеҗҲ
    v_black_delta := v_black_delta / 2; -- зҚІеҫ—pointгҒҜеҚҠеҲҶ
    if new.black_points < 420 then -- гғңгғғгғҲгҒЁгҒ®гғҸгғігғҮжҲҰгҒ§гҒҜеӢқгҒЈгҒҰгӮӮ+5
      v_black_delta := 5;
    end if;
  end if;

  -- иІ гҒ‘гҒҹвҡӘпёҸеҒҙгҒ®еҮҰзҗҶ
  v_white_giantkill := 0; -- гҒҫгҒҡйҖЈеӢқгҒҜгҒ“гӮҢгҒ§гӮ№гғҲгғғгғ—
  if v_black_games <= 20 then -- зӣёжүӢгҒ®вҡ«пёҸгҒҢеҜҫеұҖж•°гҒ®е°‘гҒӘгҒ„еј·гҒ„дәәгҒ гҒЈгҒҹгӮү
    v_white_delta := 0; -- жёӣгӮӢpointгҒҜ0
  else
    v_white_delta := -v_black_delta; -- гҒқгҒҶгҒ§гҒӘгҒ‘гӮҢгҒ°гҖҒжҷ®йҖҡгҒ«жёӣгӮүгҒ•гӮҢгӮӢ
    if v_is_black_bot then -- вҡ«пёҸгҒҢгғңгғғгғҲгҒ гҒЈгҒҹе ҙеҗҲгҒҜжёӣгӮӢpointгҒҜеҚҠеҲҶ -- гҒӘгҒҠгҖҒгғҸгғігғҮжҲҰгҒ§вҡ«пёҸгҒ®гғңгғғгғҲгҒЁеҪ“гҒҹгӮӢгҒ“гҒЁгҒҜгҒӘгҒ„
      v_white_delta := v_white_delta / 2;
    end if;
  end if;
  
  -- ------------------
  -- вҡӘпёҸгҒҢеӢқгҒЈгҒҹрҹҺү
  -- ------------------
  elsif new.result like 'W+%' then

    -- еӢқгҒЈгҒҹвҡӘпёҸеҒҙгҒ®еҮҰзҗҶ
    v_diff := new.black_points - new.white_points; -- дәҢдәәгҒ®pointгҒ®е·®
    -- гҒқгӮҢгҒһгӮҢеў—жёӣгҒҷгӮӢpoint
    v_white_delta := greatest(1, least(19,
      10 + (v_diff + case when v_diff >= 0 then 50 else -50 end) / 100
    ));
    if v_diff > 0 then -- ж јдёҠгҒ®вҡ«пёҸгҒ«еӢқгҒЈгҒҹ!зӣёжүӢгҒҢгғңгғғгғҲгҒ§гӮӮй–ўдҝӮгҒӘгҒ„
      v_white_giantkill := v_white_giantkill + 1; -- ж јдёҠйҖЈеӢқ++
      v_white_delta := v_white_delta * v_white_giantkill; -- ж јдёҠйҖЈеӢқгғңгғјгғҠгӮ№
    elsif v_is_black_bot then -- вҡ«пёҸгҒҢгғңгғғгғҲгҒ гҒЈгҒҹе ҙеҗҲгҒҜзҚІеҫ—pointгҒҜеҚҠеҲҶ -- гҒӘгҒҠгҖҒгғҸгғігғҮжҲҰгҒ§вҡ«пёҸгҒ®гғңгғғгғҲгҒЁеҪ“гҒҹгӮӢгҒ“гҒЁгҒҜгҒӘгҒ„
      v_white_delta := v_white_delta / 2;
    end if;

    -- иІ гҒ‘гҒҹвҡ«пёҸеҒҙгҒ®еҮҰзҗҶ
    v_black_giantkill := 0; -- гҒҫгҒҡйҖЈеӢқгҒҜгҒ“гӮҢгҒ§гӮ№гғҲгғғгғ—
    if v_white_games <= 20 then -- зӣёжүӢгҒ®вҡӘпёҸгҒҢеҜҫеұҖж•°гҒ®е°‘гҒӘгҒ„еј·гҒ„дәәгҒ гҒЈгҒҹгӮү
      v_black_delta := 0; -- жёӣгӮӢpointгҒҜ0
    else -- гҒқгҒҶгҒ§гҒӘгҒ‘гӮҢгҒ°гҖҒжҷ®йҖҡгҒ«жёӣгӮүгҒ•гӮҢгӮӢ
      v_black_delta := -v_white_delta;
      if v_is_white_bot then -- вҡӘпёҸгҒҢгғңгғғгғҲгҒ гҒЈгҒҹе ҙеҗҲгҒҜжёӣгӮӢpointгҒҜеҚҠеҲҶ
        v_black_delta := v_black_delta / 2;
        if new.black_points < 420 then -- 420д»ҘдёӢгҒ®гғңгғғгғҲгғҸгғігғҮжҲҰгҒ®е ҙеҗҲгҒҜиІ гҒ‘гҒҰгӮӮ-5
          v_black_delta := -5;
        end if;
      end if;
    end if;
  end if;

  -- в”Җв”Җв”Җ жӣҙж–°еҫҢгҒ®points/gumiгӮ’дәӢеүҚиЁҲз®— в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
  -- иҮӘеҲҶгҒ®гғқгӮӨгғігғҲгҒҢ0гӮ’дёӢеӣһгӮӢгҒ“гҒЁгҒҜгҒӘгҒ„
  -- жӣҙж–°еҫҢгҒ®pointsгӮ’дёҖж—ҰеҸ–еҫ—
  v_black_new_points := greatest(0, v_black_points + v_black_delta);
  v_white_new_points := greatest(0, v_white_points + v_white_delta);

  -- жӣҙж–°еүҚгҒ®gumi
  v_black_old_gumi := users.points_to_gumi(v_black_points);
  v_white_old_gumi := users.points_to_gumi(v_white_points);
  -- жӣҙж–°еҫҢгҒ®gumi
  v_black_new_gumi := users.points_to_gumi(v_black_new_points);
  v_white_new_gumi := users.points_to_gumi(v_white_new_points);

  -- гӮӮгҒ—gumiгҒҢдёҠгҒҢгӮҢгҒ°гҖҒpointsгҒҜдёӯеӨ®гҒ«з§»еӢ•!
  -- гҒ»гҒ—вҳҶвҳҶвҳҶ(gumi=17)гҒ«еҲ°йҒ”гҒ—гҒҹе ҙеҗҲгҒҜйҒ©з”ЁгҒӘгҒ—
  if (v_black_new_gumi > v_black_old_gumi) AND (v_black_new_gumi <> 17) then
    v_black_new_points := users.gumi_to_center_points(v_black_new_gumi);
  end if;

  if (v_white_new_gumi > v_white_old_gumi) AND (v_white_new_gumi <> 17) then
    v_white_new_points := users.gumi_to_center_points(v_white_new_gumi);
  end if;

  -- гӮӮгҒ—gumiгҒҢдёӢгҒҢгҒЈгҒҹе ҙеҗҲ
  -- иүІзө„(gumi=0~5)гҒ«дёӢгҒҢгӮӢе ҙеҗҲгҒҜйҒ©з”ЁгҒӘгҒ—
  if (v_black_new_gumi < v_black_old_gumi) AND (v_black_new_gumi NOT IN (0,1,2,3,4,5)) then
    v_black_new_points := users.gumi_to_center_points(v_black_new_gumi);
  end if;

  if (v_white_new_gumi < v_white_old_gumi) AND (v_white_new_gumi NOT IN (0,1,2,3,4,5)) then
    v_white_new_points := users.gumi_to_center_points(v_white_new_gumi);
  end if;


  -- в”Җв”Җв”Җ profilesжӣҙж–° в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
  -- вҡ«пёҸгҒ®profileгӮ’жӣҙж–°
  update users.profiles set
    games = games + 1, -- еҜҫеұҖж•°+1
    daily_play_count = daily_play_count + 
  case when is_premium = false then 1 else 0 end, -- з„Ўж–ҷдјҡе“ЎгҒ«гҒҜеҜҫеұҖеҲ¶йҷҗ+1
    points = v_black_new_points, -- pointжӣҙж–°
    giantkill_stroke = v_black_giantkill, -- ж јдёҠйҖЈеӢқж•°гҒ®жӣҙж–°
    gumi_index = v_black_new_gumi -- gumiжӣҙж–°
  where uid = new.black_uid
    and is_bot = false; -- botгҒ§гҒҜгҒӘгҒ„

  -- вҡӘпёҸгҒ®profileгӮ’жӣҙж–°
  update users.profiles set
    games = games + 1, -- еҜҫеұҖж•°+1
    daily_play_count = daily_play_count + 
  case when is_premium = false then 1 else 0 end, -- з„Ўж–ҷдјҡе“ЎгҒ«гҒҜеҜҫеұҖеҲ¶йҷҗ+1
    points = v_white_new_points, -- pointжӣҙж–°
    giantkill_stroke = v_white_giantkill, -- ж јдёҠйҖЈеӢқж•°гҒ®жӣҙж–°
    gumi_index = v_white_new_gumi -- gumiжӣҙж–°
  where uid = new.white_uid
    and is_bot = false; -- botгҒ§гҒҜгҒӘгҒ„

  -- botгҒ®gamesжӣҙж–°
  update users.profiles set
    games = games + 1
  where uid in (new.black_uid, new.white_uid)
    and is_bot = true;

  -- в”Җв”Җв”Җ game.recordsгҒ«insert в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
  insert into game.records (
    id, 
    black_uid, white_uid,
    moves, dead_stones, 
    result,
    black_points, white_points,
    match_type, board_size,
    created_at
  ) values (
    new.id, 
    new.black_uid, new.white_uid,
    new.moves, new.dead_stones, 
    new.result,
    new.black_points, new.white_points,
    new.match_type, new.board_size,
    now()
  ) on conflict (id) do nothing; -- idгҒҢиў«гҒЈгҒҰгҒҹгӮүrecordsгҒёгҒ®з§»еӢ•гҒҜз„ЎгҒ—

  -- в”Җв”Җв”Җ зҚІеҫ—гӮўгӮӨгӮігғігғӘгӮ№гғҲгҒ®жӣҙж–° в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
  if not v_is_black_bot then  -- botгҒ§гҒҜгҒӘгҒ„е ҙеҗҲгҖҒй»’гҒҢж–°гҒ—гҒҸгӮўгӮӨгӮігғігӮ’гӮІгғғгғҲгҒ—гҒҹгҒӢиӘҝгҒ№гӮӢ
  v_black_new_icons := users.add_icons(new.black_uid, v_black_new_gumi, v_black_games+1);
  end if;
  if not v_is_white_bot then  -- botгҒ§гҒҜгҒӘгҒ„е ҙеҗҲгҖҒзҷҪгҒҢж–°гҒ—гҒҸгӮўгӮӨгӮігғігӮ’гӮІгғғгғҲгҒ—гҒҹгҒӢиӘҝгҒ№гӮӢ
  v_white_new_icons := users.add_icons(new.white_uid, v_white_new_gumi, v_white_games+1);
  end if;

  -- в”Җв”Җв”Җ broadcastгҒ§зөӮеұҖйҖҡзҹҘ в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
  perform realtime.send(
    jsonb_build_object(
      'result',           new.result,
      'dead_stones',      new.dead_stones
    ),
    'finished', -- гӮӨгғҷгғігғҲеҗҚ
    'game:' || new.id::text, -- гғҒгғЈгғігғҚгғ«еҗҚ
    false
  );

  perform realtime.send(
    jsonb_build_object(
      'delta',      v_black_delta,
      'new_points',     v_black_new_points,
      'new_gumi_index', v_black_new_gumi,
      'new_acquired_icons', v_black_new_icons
    ),
    'finished', -- гӮӨгғҷгғігғҲеҗҚ
    'user:' || new.black_uid::text, -- гғҒгғЈгғігғҚгғ«еҗҚ
    false
  );

  perform realtime.send(
    jsonb_build_object(
      'delta',      v_white_delta,
      'new_points',     v_white_new_points,
      'new_gumi_index', v_white_new_gumi,
      'new_acquired_icons', v_white_new_icons
    ),
    'finished', -- гӮӨгғҷгғігғҲеҗҚ
    'user:' || new.white_uid::text, -- гғҒгғЈгғігғҚгғ«еҗҚ
    false
  );

  -- в”Җв”Җв”Җ game.playingгҒӢгӮүdelete в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
  delete from game.playing where id = new.id;

  return null;
end;
