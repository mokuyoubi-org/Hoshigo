CREATE OR REPLACE FUNCTION users.points_to_gumi(p_points smallint)
 RETURNS smallint
 LANGUAGE sql
 IMMUTABLE
AS $function$
  select cast(greatest(0, (
    select count(*) - 1
    from (values
      (0), (20), (60), (120), (200), (300),
      (420), (560), (720), (900), (1100), (1300),
      (1500), (1700), (1900), (2100), (2300), (2500)
    ) as t(threshold)
    where threshold <= p_points
  )) as smallint);
