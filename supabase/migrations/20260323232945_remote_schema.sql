CREATE OR REPLACE FUNCTION game.cancel_waiting()
 RETURNS jsonb
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
declare
  v_player_uid uuid := auth.uid();
  v_match      game.playing%rowtype;
  v_is_black   boolean;
begin
  -- в‘  гҒҫгҒҡ playing гӮ’зўәиӘҚ
  select *
  into v_match
  from game.playing
  where (black_uid = v_player_uid or white_uid = v_player_uid)
    and status = 'playing'
  limit 1;

  if found then
    -- playing гҒ«гҒ„гҒҹгӮүгҖҒгҒҷгҒ§гҒ«еҜҫеұҖгҒҜе§ӢгҒҫгҒЈгҒҰгҒ„гӮӢгҒ®гҒ§еҜҫеұҖжғ…е ұгӮ’иҝ”гҒҷ
