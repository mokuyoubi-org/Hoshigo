          result = 'W+C'
      where id = rec.id;

    elsif (not v_is_white_bot) -- вҡӘпёҸгҒҢгғңгғғгғҲгҒ§гҒҜгҒӘгҒҸгҖҒ
       and rec.white_last_seen is not null
       and extract(epoch from (now() - rec.white_last_seen)) > 30 then -- вҡӘпёҸгҒ®жңҖеҫҢгҒ®гғҸгғјгғҲгғ“гғјгғҲгҒӢгӮү30з§’д»ҘдёҠзөҢгҒЈгҒҰгҒ„гӮӢгҒӘгӮү
      update game.playing
