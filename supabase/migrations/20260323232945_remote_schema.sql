          result = 'B+T'
      where id = rec.id;

    -- ------------------------------
    -- жҺҘз¶ҡеҲҮгӮҢеҲӨе®ҡ
    -- ------------------------------
    elsif (not v_is_black_bot) -- вҡ«пёҸгҒҢгғңгғғгғҲгҒ§гҒҜгҒӘгҒҸгҖҒ
       and rec.black_last_seen is not null
