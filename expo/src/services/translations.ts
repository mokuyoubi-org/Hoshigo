// @/src/services/translations.ts
export type Lang = "ja" | "en" | "zh" | "ko";
type Section = keyof typeof translations;
type SectionKey<S extends Section> = keyof (typeof translations)[S]["en"];
export type TranslationKey = {
  [S in Section]: `${S}.${string & SectionKey<S>}`;
}[Section];

export function t(
  sectionAndKey: TranslationKey,
  lang: Lang,
  params?: Record<string, string | number>,
): string {
  const [section, key] = sectionAndKey.split(".") as [Section, string];

  const value =
    (translations[section][lang] as any)[key] ??
    (translations[section].en as any)[key];

  if (!params) return value;

  return Object.entries(params).reduce(
    (str, [k, v]) => str.replaceAll(`{{${k}}}`, String(v)),
    value,
  );
}
